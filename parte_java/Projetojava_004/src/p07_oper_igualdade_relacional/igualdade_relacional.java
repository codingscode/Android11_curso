
package p07_oper_igualdade_relacional;


public class igualdade_relacional {

   public static void main(String[] args) {
      int a = 2, b = 5; // experimentar alterar valores
      
      System.out.println("a == b : " + (a==b));
      System.out.println("a != b : " + (a!=b));
      System.out.println("a > b  : " + (a>b));
      System.out.println("a >= b : " + (a>=b));
      System.out.println("a < b  : " + (a<b));
      System.out.println("a <= b : " + (a<=b));
      
   }

}

/*
a == b : false
a != b : true
a > b  : false
a >= b : false
a < b  : true
a <= b : true


*/

